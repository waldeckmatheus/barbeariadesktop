    public void setaNaTelaOsDados(int cod) {

        FuncionarioDao fdao = new FuncionarioDao();

        List<Funcionario> lista = fdao.pesquisarPorCodigo(cod);
        for (Funcionario p : lista) {
            if (p.getFunCod() == cod) {
                txtnome.setText(p.getFunNome());
                txtlogin.setText(p.getFunLogin());
                txtsenha.setText(p.getFunSenha());
                txtpercent.setText(String.valueOf(p.getFunPercent()));
            }
            cbfuncao.setSelectedItem(p.getFuncao());

        }

    }

