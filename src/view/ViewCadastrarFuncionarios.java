    private void btsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsalvarActionPerformed
        if (opcao == 1) {
            if (txtnome.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "O campo Nome e Obrigatório ,Favor Preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtlogin.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "O campo Login e Obrigatório ,Favor Preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtsenha.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "O campo Senha e Obrigatório ,Favor Preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtpercent.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "O campo Percentual e Obrigatório ,Favor Preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Funcionario f = new Funcionario();
            f.setFunNome(txtnome.getText());
            f.setFunLogin(txtlogin.getText());
            f.setFunSenha(txtsenha.getText());

            f.setFuncao(cbfuncao.getSelectedItem().toString());
            f.setFunPercent(Integer.parseInt(txtpercent.getText()));

            FuncionarioDao fdao = new FuncionarioDao();
            fdao.salvar(f);

            hide();
        } else if (opcao == 2) {
            if (txtnome.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "O campo Nome e Obrigatório ,Favor Preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtlogin.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "O campo Login e Obrigatório ,Favor Preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtsenha.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "O campo Senha e Obrigatório ,Favor Preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (txtpercent.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "O campo Percentual e Obrigatório ,Favor Preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
            Funcionario f = new Funcionario();
            f.setFunCod(pcod);
            f.setFunNome(txtnome.getText());
            f.setFunLogin(txtlogin.getText());
            f.setFunSenha(txtsenha.getText());

            f.setFuncao(cbfuncao.getSelectedItem().toString());
            f.setFunPercent(Integer.parseInt(txtpercent.getText()));

            FuncionarioDao fdao = new FuncionarioDao();
            fdao.alterar(f);

            hide();
        }


    }//GEN-LAST:event_btsalvarActionPerformed

