    private void setaNaTelaOsDados(int cod) {
        List<Produto> lista = dao.pesquisarPorCodigo(cod);
        for (Produto p : lista) {
            if (p.getProdCod() == cod) {
                txtnome.setText(p.getProdNome());
                txtdescricao.setText(p.getProdNome());
                txtvalorcompra.setText(String.valueOf(p.getProdValorCompra()));
                txtvalorvenda.setText(String.valueOf(p.getProdValorVenda()));
            }

        }

    }

