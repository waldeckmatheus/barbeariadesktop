    public void validar() throws InvalidFieldException {
        if (txtnome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo nome é obrigatório, por favor preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
            throw new InvalidFieldException("nome");
        }
        if (txtvalorcompra.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo valor é obrigatório, por favor preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
            throw new InvalidFieldException("prodValorCompra");
        }
        if (txtvalorvenda.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo valor é obrigatório, por favor preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
            throw new InvalidFieldException("prodValorVenda");
        }
    }

